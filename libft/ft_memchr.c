#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*src;

	src = (char *)s;
	i = 0;
	while (i < n)
	{
		if (src[i] == (char)c)
		{
			return (src + i);
		}
		i++;
	}
	return (0);
}
